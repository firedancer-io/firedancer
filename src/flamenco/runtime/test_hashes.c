/* Helper function to check if two hashes are equal */
static int
fd_lthash_equal( fd_lthash_value_t const * a, fd_lthash_value_t const * b ) {
  return memcmp( a->bytes, b->bytes, FD_LTHASH_LEN_BYTES ) == 0;
}

static int
fd_hash_equal( fd_hash_t const * a, fd_hash_t const * b ) {
  return memcmp( a->uc, b->uc, FD_HASH_FOOTPRINT ) == 0;
}

