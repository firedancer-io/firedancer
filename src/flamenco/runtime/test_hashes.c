/* Define global test data */
static fd_account_meta_t test_meta;
static uchar test_data[64];

static void
test_fd_hashes_update_lthash( void ) {
  FD_LOG_NOTICE(( "Testing fd_hashes_update_lthash" ));

  /* Test the lthash update logic without a real bank structure */

  /* Create test data */
  fd_pubkey_t pubkey;
  memset( &pubkey, 0x22, sizeof(fd_pubkey_t) );

  test_meta.lamports = 5000UL;
  test_meta.executable = 0;
  memset( test_meta.owner, 0x33, FD_PUBKEY_FOOTPRINT );

  memset( test_data, 0x44, 64 );

  /* Initialize a bank lthash */
  fd_lthash_value_t bank_lthash;
  memset( bank_lthash.bytes, 0x11, FD_LTHASH_LEN_BYTES );

  /* Calculate previous account hash */
  fd_lthash_value_t prev_hash;
  memset( prev_hash.bytes, 0x55, FD_LTHASH_LEN_BYTES );

  /* Store initial bank lthash */
  fd_lthash_value_t initial_lthash;
  memcpy( &initial_lthash, &bank_lthash, sizeof(fd_lthash_value_t) );

  /* Simulate what fd_hashes_update_lthash does */

  /* Subtract the old hash */
  fd_lthash_sub( &bank_lthash, &prev_hash );

  /* Calculate new hash */
  fd_lthash_value_t new_hash;
  fd_hashes_account_lthash( &pubkey, &test_meta, test_data, 64UL, &new_hash );

  /* Add new hash */
  fd_lthash_add( &bank_lthash, &new_hash );

  /* Verify the result is correct */
  fd_lthash_value_t expected_lthash;
  memcpy( &expected_lthash, &initial_lthash, sizeof(fd_lthash_value_t) );
  fd_lthash_sub( &expected_lthash, &prev_hash );
  fd_lthash_add( &expected_lthash, &new_hash );

  FD_TEST( fd_lthash_equal( &bank_lthash, &expected_lthash ) );

  /* Test with zero previous hash (new account) */
  fd_lthash_value_t zero_prev;
  fd_lthash_zero( &zero_prev );

  memcpy( &bank_lthash, &initial_lthash, sizeof(fd_lthash_value_t) );
  fd_lthash_sub( &bank_lthash, &zero_prev );  /* Should be no-op */
  fd_lthash_add( &bank_lthash, &new_hash );

  memcpy( &expected_lthash, &initial_lthash, sizeof(fd_lthash_value_t) );
  fd_lthash_add( &expected_lthash, &new_hash );

  FD_TEST( fd_lthash_equal( &bank_lthash, &expected_lthash ) );

  FD_LOG_NOTICE(( "test_fd_hashes_update_lthash passed" ));
}

